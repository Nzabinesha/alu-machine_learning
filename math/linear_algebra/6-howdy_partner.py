#!/usr/bin/env python3
"""Module that concatenates two arrays."""


def cat_arrays(arr1, arr2):
    """Concatenate two arrays.

    Args:
        arr1: a list of ints/floats.
        arr2: a list of ints/floats.

    Returns:
        A new list containing the concatenation of arr1 and arr2.
    """
    return arr1 + arr2
